public class Temel_matamatik_operetorleri {
    public static void main(String[] args){
        System.out.println(3+4);
        System.out.println(3-4);
        System.out.println(3*4);
        System.out.println(3/4);//int deger doner
        System.out.println(3d/4);//double deger doner
        System.out.println(4%3);
        int a=1;
        a=a+1;//a=2
        a+=1;//a=3
        a++;//a=4
        a--;//a=3
        ++a;//a=4 ama yazdırırken once a artara gosterir c deki ile aynı
        System.out.println(a++);//a=4
        System.out.println(a);//a=5
        System.out.println(++a);//a=6
        System.out.println(3+4*5);//23 işlem onceliiği var
    }
}
